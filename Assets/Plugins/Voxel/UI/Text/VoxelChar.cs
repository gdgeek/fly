using UnityEngine;
using System.Collections;
namespace GDGeek{
	public class VoxelChar : MonoBehaviour {
		private char _char;
		public VoxelMesh _mesh = null;
		public void Awake(){
			if (_mesh == null) {
				_mesh = this.gameObject.GetComponent<VoxelMesh> ();
			}
		}
		public char ch{
			set{
				_char = value;
			}
			get{
				return _char;
			}
		}
		public void setMainColor(Color color){
			_mesh.setMainColor (color);
		}

		public void setLightColor(Color color){
			_mesh.setLightColor (color);
		}
		public void setLayer(int layer){
			_mesh.setLayer (layer);
		}
	}
}
